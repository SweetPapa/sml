"""SML-augmented model wrapper for EleutherAI lm-evaluation-harness.

Subclasses HFLM to prepend SML context blocks (generated by the Encoder
from the Bible) to every benchmark prompt before the base model sees it.
Also injects the SML system prompt via the model's chat template so the
model is primed to interpret SML blocks — matching training-time behaviour.

Usage (CLI):
    lm_eval --include_path sml/evaluation --model sml_hf --model_args pretrained=sweetpapa/sml-qwen2.5-3b-phase2,bible_path=data/sml_bible.db --tasks hellaswag,arc_easy

Usage (Python):
    from sml.evaluation.sml_harness import SMLAugmentedHFLM
    import lm_eval

    model = SMLAugmentedHFLM(
        pretrained="sweetpapa/sml-qwen2.5-3b-phase2",
        bible_path="data/sml_bible.db",
    )
    results = lm_eval.simple_evaluate(model=model, tasks=["hellaswag"])
"""

import functools
import logging
from typing import Optional

from tqdm import tqdm

from lm_eval.api.registry import register_model
from lm_eval.models.huggingface import HFLM

logger = logging.getLogger(__name__)

# The system prompt the model was trained with — MUST be present at eval time
SML_SYSTEM_PROMPT = (
    "You are an AI assistant that uses Structured Markup Language (SML) "
    "context to ground your reasoning. Always analyze the provided SML "
    "block before answering."
)


@register_model("sml_hf")
class SMLAugmentedHFLM(HFLM):
    """HuggingFace causal LM that injects SML context into every prompt.

    Extra ``model_args`` (comma-separated key=value via CLI, or kwargs in Python):

        bible_path   - path to the SML Bible SQLite DB
                       (default: data/sml_bible.db from sml.config)
        spacy_model  - spaCy model for the encoder (default: en_core_web_sm)
        sml_cache    - LRU cache size for encoded prompts (default: 4096)
        max_encode   - max chars sent to the encoder; takes the tail of the
                       prompt so the actual question is always captured
                       (default: 2048)
    """

    def __init__(
        self,
        bible_path: Optional[str] = None,
        spacy_model: str = "en_core_web_sm",
        sml_cache: int = 4096,
        max_encode: int = 2048,
        **kwargs,
    ):
        # HFLM.__init__ handles model/tokenizer loading
        super().__init__(**kwargs)

        from sml.encoder.encoder import SMLEncoder
        from sml.config import BIBLE_DB_PATH

        bible = bible_path or str(BIBLE_DB_PATH)
        self._sml_encoder = SMLEncoder(bible, spacy_model=spacy_model)
        self._max_encode = int(max_encode)

        # LRU cache keyed on the text snippet — avoids re-encoding
        # identical prompts across answer-choice permutations
        self._encode_cached = functools.lru_cache(maxsize=int(sml_cache))(
            self._sml_encoder.encode
        )

        # Pre-compute the system prompt prefix using the model's chat template
        # so every eval prompt starts the same way the training data did.
        self._system_prefix = self._build_system_prefix()

        logger.info(
            "SML encoder ready  bible=%s  max_encode=%d  cache=%d  "
            "system_prefix_len=%d",
            bible, self._max_encode, int(sml_cache),
            len(self._system_prefix),
        )

    # ── System prompt injection ────────────────────────────────────────

    def _build_system_prefix(self) -> str:
        """Render the SML system prompt through the model's chat template.

        Returns the token string that should be prepended to every eval
        prompt so the model sees the same system-message framing it was
        trained with.
        """
        try:
            # Render a minimal [system, user=""] conversation and take
            # everything up to (but not including) the user content.
            messages = [
                {"role": "system", "content": SML_SYSTEM_PROMPT},
                {"role": "user", "content": ""},
            ]
            rendered = self.tokenizer.apply_chat_template(
                messages, tokenize=False, add_generation_prompt=False,
            )
            # The rendered string ends with the empty-user turn.  We want
            # everything before that empty content so we can splice the
            # actual eval prompt in its place.  Find the last occurrence
            # of the empty-user boundary and trim there.
            #
            # For Qwen-style templates this looks like:
            #   <|im_start|>system\n{sys_prompt}<|im_end|>\n<|im_start|>user\n
            # We want to keep through the user-turn header.
            #
            # Strategy: render again with a unique sentinel as user content,
            # then split on the sentinel.
            sentinel = "<<<__SML_SPLIT__>>>"
            messages[1]["content"] = sentinel
            rendered = self.tokenizer.apply_chat_template(
                messages, tokenize=False, add_generation_prompt=False,
            )
            prefix = rendered.split(sentinel)[0]
            logger.info("System prefix built (%d chars)", len(prefix))
            return prefix
        except Exception:
            logger.warning(
                "Could not build system prefix from chat template; "
                "falling back to raw text prefix",
                exc_info=True,
            )
            return SML_SYSTEM_PROMPT + "\n\n"

    def _inject_system_prompt(self, text: str) -> str:
        """Prepend the system prompt prefix if not already present."""
        # Avoid double-injection when the prompt already contains
        # the system message (e.g. from a task that formats chat).
        if SML_SYSTEM_PROMPT in text:
            return text
        return self._system_prefix + text

    # ── SML injection ──────────────────────────────────────────────────

    def _get_snippet(self, text: str) -> str:
        """Extract the tail of the prompt for encoding."""
        return text[-self._max_encode :] if len(text) > self._max_encode else text

    def _inject_sml(self, text: str, sml_block: str) -> str:
        """Prepend a pre-computed SML block to the prompt."""
        return sml_block + "\n" + text

    def _encode_batch(self, requests, extract_fn):
        """Encode SML for all requests, with progress bar and caching.

        extract_fn(req) -> text to encode.
        Returns list of SML block strings (empty string on failure).
        """
        snippets = [self._get_snippet(extract_fn(req)) for req in requests]

        # Deduplicate to avoid redundant spaCy calls
        unique_snippets = list(set(snippets))
        sml_map = {}

        desc = f"SML encode ({len(unique_snippets)} unique / {len(snippets)} total)"
        for snippet in tqdm(unique_snippets, desc=desc, disable=False):
            try:
                sml_map[snippet] = self._encode_cached(snippet)
            except Exception:
                if not getattr(self, "_warned_encode", False):
                    logger.warning(
                        "SML encoding failed for a prompt, passing unchanged",
                        exc_info=True,
                    )
                    self._warned_encode = True
                sml_map[snippet] = ""

        ci = self._encode_cached.cache_info()
        logger.info("SML cache: %d hits / %d misses", ci.hits, ci.misses)

        return [sml_map[s] for s in snippets]

    # ── Override the three LM API entry-points ─────────────────────────
    #
    # Each override:
    #   1. Injects the SML system prompt (matching training framing)
    #   2. Prepends SML-encoded context from the Bible

    def loglikelihood(self, requests, disable_tqdm=False):
        sml_blocks = self._encode_batch(requests, lambda r: r.args[0])
        for req, sml in zip(requests, sml_blocks):
            ctx, cont = req.args
            ctx = self._inject_system_prompt(ctx)
            if sml:
                ctx = self._inject_sml(ctx, sml)
            req.arguments = (ctx, cont)
        return super().loglikelihood(requests, disable_tqdm)

    def loglikelihood_rolling(self, requests, disable_tqdm=False):
        sml_blocks = self._encode_batch(requests, lambda r: r.args[0])
        for req, sml in zip(requests, sml_blocks):
            (text,) = req.args
            text = self._inject_system_prompt(text)
            if sml:
                text = self._inject_sml(text, sml)
            req.arguments = (text,)
        return super().loglikelihood_rolling(requests, disable_tqdm)

    def generate_until(self, requests, disable_tqdm=False):
        sml_blocks = self._encode_batch(requests, lambda r: r.args[0])
        for req, sml in zip(requests, sml_blocks):
            ctx, gen_kwargs = req.args
            ctx = self._inject_system_prompt(ctx)
            if sml:
                ctx = self._inject_sml(ctx, sml)
            req.arguments = (ctx, gen_kwargs)
        return super().generate_until(requests, disable_tqdm)

    # ── Diagnostics ────────────────────────────────────────────────────

    @property
    def sml_cache_info(self):
        """Return LRU cache stats for SML encoding."""
        return self._encode_cached.cache_info()

    def __del__(self):
        if hasattr(self, "_sml_encoder"):
            self._sml_encoder.close()
